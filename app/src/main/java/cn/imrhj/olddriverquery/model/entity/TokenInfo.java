public class TokenInfo {

    /**
     * key : f8716ab6-712a-4622-be5b-fd950326acb2
     * msg : sucess
     * code : 1
     */

    private String key;
    private String msg;
    private int code;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }
}
